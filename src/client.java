
public class client {
	
	private int id;
	private String name;
	
	public client(int id, String name) {
		this.id = id;
		this.name = name;
	}
	
	//Getter and Setter Methods
	public int getID() {
		return this.id;
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
}
